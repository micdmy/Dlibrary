#define NUCLEO_USART2_PORT GPIOA
#define NUCLEO_USART2_PIN_TX 0x02
#define NUCLEO_USART2_PIN_RX 0x03
#define NUCLEO_USART2_PIN_TX_MASK 1 << NUCLEO_USART2_PIN_TX
#define NUCLEO_USART2_PIN_RX_MASK 1 << NUCLEO_USART2_PIN_RX
#define NUCLEO_USART2_ALT_FUNC	4ul
