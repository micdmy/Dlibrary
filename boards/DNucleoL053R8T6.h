#define NUCLEO_BUTTON_PIN_MASK 1 << NUCLEO_BUTTON_PIN

#define NUCLEO_LED_GREEN_IOPENER_BIT RCC_IOPENR_GPIOAEN // Bit for clk for GPIOA enabling.
